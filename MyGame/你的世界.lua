[[物品]]
完成平行世界任务会有奖励：各种不同的经验值，收集品，只可以用点券购买的物品

每个故事里面都会有一些收集品，大约最多10个，官方认可更好的故事收集品越多，等你回来后，有类似收集库的地方，里面东西越多就可以随着时间产生更多的经验值什么的。有的收集品会影响其他的故事。
收集品每个每人只能拥有1个，但是也可以交易出去。

物品：只有点券可以兑换的物品，比如回血回蓝的增强实力的等，但是每次故事使用数量是有限制的

[[创作]]
在等到了一定的程度后，你们有了自己挖掘（生成）平行世界的能力，你可以创建世界等待别人来完成。每有人进入你的世界，在里面消耗时间，你会获得你们组织对你的奖励，甚至包括点券。
当你进入了别人的世界后，可以帮助这个人来提高这个世界的完成度。在编辑模式下，故事的每个段落后面可以添加评论。写故事的人有要求要采纳建议或者反驳，如果建议被采纳那么提出建议的人在写故事人获得收益时自己也能获得一小份。也可以再每章节后追加下面的故事（但是这个怎么和原故事作者合作还没想好）。

不同的平行世界有不同的类型，有些类型是故事作者自己选择的，有些是写好以后根据故事系统评估出的：是否可以使用特殊技能；是否需要合作

